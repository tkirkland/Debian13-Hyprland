#!/usr/bin/env bash
set -euo pipefail
cd "$(dirname "$0")/.."
source tests/test-helpers.sh

echo "test: Hyprland Lua config generation"
tmp="$(mktemp -d)"
trap 'rm -rf "${tmp}"' EXIT

info() { :; }
fatal() {
  printf 'fatal: %s\n' "$*" >&2
  return 1
}
in_target() { :; }

source scripts/60-hyprland.sh

TARGET="${tmp}/target"
TARGET_USERNAME="tester"
HYPR_AUTOLOGIN=1

# The user config is upstream's example/hyprland.lua from the staged
# Hyprland source tree (resolved tag), with the installer's first-run
# welcome block appended. Stage a representative fake example.
mkdir -p "${TARGET}${HYPR_SRC_DIR}/hyprland/example"
cat >"${TARGET}${HYPR_SRC_DIR}/hyprland/example/hyprland.lua" <<'EOF'
-- Upstream example config (fake for tests).
local mainMod = "SUPER"
hl.monitor({ output = "", mode = "preferred" })
hl.bind(mainMod .. " + Q", hl.dsp.exec_cmd("kitty"))
hl.bind(mainMod .. " + C", hl.dsp.window.close())
EOF

configure_session

lua_config="${TARGET}/home/${TARGET_USERNAME}/.config/hypr/hyprland.lua"
legacy_config="${TARGET}/home/${TARGET_USERNAME}/.config/hypr/hyprland.conf"

if [[ -f "${lua_config}" ]]; then
  echo "  ok: user hyprland.lua exists"
  config="$(<"${lua_config}")"
  assert_contains "${config}" "Upstream example config (fake for tests)." \
    "upstream example is the base config"
  assert_contains "${config}" \
    'hl.bind(mainMod .. " + Q", hl.dsp.exec_cmd("kitty"))' \
    "upstream content survives verbatim"
  assert_contains "${config}" "Hypr-Deb additions" \
    "installer block appended after the example"
  assert_contains "${config}" "/usr/local/bin/hyprland-welcome" \
    "welcome app launched by absolute path"
  assert_contains "${config}" ".welcome-shown" \
    "welcome launch gated by the acknowledgment marker"
  # Acknowledgment semantics: the welcome app replaces upstream's
  # autogenerated-config notice, so the marker lands only when the app
  # exits cleanly (user closed it) — app first, && touch after.
  # shellcheck disable=SC2016  # literal needle; $marker expands at login
  assert_contains "${config}" \
    '/usr/local/bin/hyprland-welcome && touch "$marker"' \
    "marker touched only after the app is acknowledged"
  if [[ "${config}" == *"autogenerated = true"* ]]; then
    echo "  FAIL: full upstream config must not set the autogenerated flag" >&2
    TEST_FAILURES=$((TEST_FAILURES + 1))
  else
    echo "  ok: no autogenerated warning bar with the full config"
  fi
else
  echo "  FAIL: user hyprland.lua does not exist" >&2
  TEST_FAILURES=$((TEST_FAILURES + 1))
fi

if [[ -e "${legacy_config}" ]]; then
  echo "  FAIL: legacy hyprland.conf must not be generated" >&2
  TEST_FAILURES=$((TEST_FAILURES + 1))
else
  echo "  ok: legacy hyprland.conf is not generated"
fi

# A missing staged example is a loud failure, not a silent fallback.
# fatal exits in the real installer (lib/01-log.sh); mirror that here.
assert_fails "missing staged example config is fatal" bash -c "
  info() { :; }
  fatal() { exit 1; }
  in_target() { :; }
  source scripts/60-hyprland.sh
  TARGET='${tmp}/empty-target'
  TARGET_USERNAME='tester'
  HYPR_AUTOLOGIN=1
  configure_session
"

ver_body="$(bash -c 'source lib/00-config.sh; source lib/01-log.sh
  source scripts/90-verify.sh
  declare -f phase_verify' 2>/dev/null || true)"
assert_contains "${ver_body}" "welcome app installed" \
  "verify checks the welcome binary exists"

finish_test
